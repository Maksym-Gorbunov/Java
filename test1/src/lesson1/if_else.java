package lesson1;

public class if_else {
    public static void main(String[] args) {
        int x = 13;
        boolean y = true;

        if(x == 13 && !y){
            System.out.println("yes, x == 13");
        } else {
            System.out.println("no, x != 13");
        }
    }
}

